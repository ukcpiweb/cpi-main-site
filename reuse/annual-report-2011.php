<p>The Deputy Prime Minister Nick Clegg says: "This first elite technology and innovation centre in high value manufacturing will support our objective to rebalance the economy by underpinning current UK manufacturing strengths through the development and deployment of novel technologies into sectors such as aerospace, automotive and pharmaceuticals, whilst at the same time driving the UK into leading positions in new high growth markets by creating a platform for innovative SMEs to work with larger companies in developing technologies such as plastic electronics, fibre reinforced polymer composites and biotechnology." The Business Secretary, Vince Cable, says: "Each of</p>
