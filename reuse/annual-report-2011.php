<p>CPI's work to develop and implement this facility has been deservedly recognised by the industry, having received Highly Commended status at the IChemE Awards, following CPI's shortlisting for the BioProcessing Award.</p>

<p>The UK Government has cited Anaerobic Digestion (AD) as being a vital technology for achieving its goal of becoming the greenest Government ever. As waste increasingly becomes viewed as a resource to be harnessed, AD's importance in converting waste to</p>
</div>

<div class="columnthree">
