<p>CPI's ADDC embodies this approach. Part-funded by the Department of Energy and Climate Change (DECC), the ADDC is an open access facility, designed, built and operated by CPI. The ADDC helps organisations to develop tailored AD processes and
</p>

</div>
<div class="columnthree">
<p>technologies. It offers a rapidly reconfigurable, "plug and play" facility which explores alternatives to traditional waste disposal to create cleaner, sustainable AD processes</p>

