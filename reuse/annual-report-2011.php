<li>Improving process control</li>
<li>Increasing biogas yield from various feedstocks, including co-digestates</li>
<li>Further developing pre- and post-treatment technologies to improve yields</li>
<li>Enhancing digestate properties to develop high quality natural soil conditioners/fertilizers</li>
<li>Improving effluent water quality</li>
<li>Developing purification and monitoring
processes to allow injection of biogas into
the gas grid</li>
</ul></p>
<p>In this way, CPI's ADDC will attract wealth and economic opportunities to the UK.</p>

